#include <iostream>
using namespace std;

class RotateMatrix {
    int a[10][10];
    int n;

public:
    void input() {
        cin >> n;
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                cin >> a[i][j];
            }
        }
    }

    void rotate() {
        for (int i = 0; i < n; i++) {
            for (int j = i; j < n; j++) {
                int t = a[i][j];
                a[i][j] = a[j][i];
                a[j][i] = t;
            }
        }

        for (int i = 0; i < n; i++) {
            int l = 0, r = n - 1;
            while (l < r) {
                int t = a[i][l];
                a[i][l] = a[i][r];
                a[i][r] = t;
                l++;
                r--;
            }
        }

        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                cout << a[i][j] << " ";
            }
            cout << endl;
        }
    }
};

int main() {
    RotateMatrix r;
    r.input();
    r.rotate();
    return 0;
}
